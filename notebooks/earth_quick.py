    # Check framework constraints for sample weight handling
    if name in ["Gradient Boosting"]:
        model.fit(X_train.toarray(), y_train, sample_weight=sample_weights)
    elif name in ["Hist Gradient Boosting"]:
        model.fit(X_train.toarray(), y_train)
    elif name in ["XGBoost"]:
        model.fit(X_train, y_train, sample_weight=sample_weights)
