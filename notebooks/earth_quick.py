# Soft Deduplication: Sirf exact duplicate coordinates aur target ko drop karenge
# Taaki structural rows safe rahein aur matrix scale up ho ske
df = df.drop_duplicates(subset=["latitude", "longitude", "magnitude", "date_time", "alert"])
df = df.reset_index(drop=True)
print(f"Rows retained after optimized deduplication: {len(df)}")

# Target Variable Encoding
label_encoder = LabelEncoder()
df["alert_encoded"] = label_encoder.fit_transform(df["alert"])

print("\nOptimized Class Distribution:")
print(df["alert"].value_counts())

# =========================================================
# 2. DATE & TIME PATTERNS
# =========================================================
df["date_time"] = pd.to_datetime(df["date_time"], errors="coerce", dayfirst=True)
df["year"] = df["date_time"].dt.year.fillna(df["date_time"].dt.year.median())
df["month"] = df["date_time"].dt.month.fillna(1)
df["day"] = df["date_time"].dt.day.fillna(1)
df["weekday"] = df["date_time"].dt.weekday.fillna(0)

# =========================================================
# 3. CATEGORICAL ENCODING
# =========================================================
categorical_cols = ["magType", "continent", "country"]
encoders = {}

for col in categorical_cols:
    df[col] = df[col].astype(str).fillna("Unknown")
    le = LabelEncoder()
    df[col] = le.fit_transform(df[col])
    encoders[col] = le

# =========================================================
# 4. NUMERIC PROCESSING & IMPUTATION
# =========================================================
num_cols = ["magnitude", "cdi", "mmi", "tsunami", "sig", "nst", "dmin", "gap", "depth", "latitude", "longitude"]
imputer = SimpleImputer(strategy="median")
df[num_cols] = imputer.fit_transform(df[num_cols])

# =========================================================
# 5. ADVANCED GEOGRAPHIC & PHYSICAL FEATURE ENGINEERING
# =========================================================
df["mag_depth_ratio"] = df["magnitude"] / (df["depth"] + 1.0)
df["mag_squared"] = df["magnitude"] ** 2
df["depth_log"] = np.log1p(df["depth"])
df["sig_log"] = np.log1p(df["sig"])
df["mag_sig"] = df["magnitude"] * df["sig"]
df["mmi_cdi_diff"] = np.abs(df["mmi"] - df["cdi"])
df["sig_per_mag"] = df["sig"] / (df["magnitude"] + 0.1)
df["abs_lat"] = df["latitude"].abs()
df["lat_lon_interaction"] = df["latitude"] * df["longitude"]

extra_cols = [
    "mag_depth_ratio", "mag_squared", "depth_log", "sig_log",
    "mag_sig", "mmi_cdi_diff", "sig_per_mag", "abs_lat",
    "lat_lon_interaction", "year", "month", "day", "weekday",
    "magType", "continent", "country"
]

all_features = num_cols + extra_cols
